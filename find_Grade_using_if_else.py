#WAP using if else caculating grades of a student

grade = int(input("Enter grade: ")) 
if grade >= 90: 
    print("You passed the class with grade A.")
elif grade >= 80: 
    print("You passed the class with grade B.")
elif grade >= 70: 
    print("You passed the class with grade C.")
else: 
    print("You did not pass the class :(")

print("Thanks for attending.")
